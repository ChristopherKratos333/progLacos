'''
Desenvolver um programa que imprima a tabuada de 3 a 6.
'''
print("Tabuada de 3:")
cont = 1
while cont <= 10:
    mult = 3 * cont
    print(f"3 . {cont} = {mult}")
    cont = cont + 1

